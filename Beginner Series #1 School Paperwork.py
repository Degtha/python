# Your task is to calculate how many blank pages do you need. If n < 0 or m < 0 return 0.

# Example:
# n= 5, m=5: 25
# n=-5, m=5:  0
# Waiting for translations and Feedback! Thanks!

def paperwork(n, m):
    # Happy Coding! ^_^
    if n < 0 or m < 0:
        return 0
    else :
        return n * m